sub pow {
	my ($n, $value) = @_;
	my $result = 1;
	while ($n > 0) {
		$result *= $value;
		$n --;
	} # while 
	return $result;
} # pow

sub log2 {
	my ($pow2) = @_;
	return length(substr(sprintf("%b", $pow2), 1)); # count number of zeroes
} # log2

sub revbits {
	my ($width, $value) = @_;
	# $width = &log2($width);
	return oct("0b" . join('', reverse(split(//, sprintf("%0${width}b", $value)))) ); 
} # revbits

